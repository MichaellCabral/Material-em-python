lista_num = [10,20,30,40,50]
print('Todos os elementos da lista sao {}'.format(lista_num))
min(lista_num)
print('o numero minimo da lista eh: {}'.format(min(lista_num)))#mostra o numero minimo da lista
max(lista_num)
print('o numero maximo da lista eh: {}'.format(max(lista_num)))#mostra o numero maximo da lista
sum(lista_num)
print('a soma de todos os numeros da lista eh: {}'.format(sum(lista_num)))#mostra a soma de todos os numeros da lista
